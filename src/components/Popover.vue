<template>
  <span v-if="visible" class="popover" :style="elementStyle">
    <slot />
  </span>
</template>
<script lang="ts">
import _ from 'lodash';
import Vue from 'vue';
import { Component, Prop, Watch } from 'vue-property-decorator';

import { Alignment } from '@/components/constants';
import * as DOMUtil from '@/components/domutil';

/**
 * We use weak typing as it is used to define CSS rules
 * And that we're not forced to define all four properties
 */
interface ElementPosition {
  top?: string | number;
  bottom?: string | number;
  left?: string | number;
  right?: string | number;
}

/**
@type {VueComponent}
@name Popover

@description
Implement a popover component
@params {Boolean} visible sets the popover visibility. If `true` the popover is mounted.

@params {String} [align = 'center'] sets the popover's default alignment:
  - possible values work like the text-align CSS property
  - justify adapts the popover to its parent (same width and horizontal position)

@params {Boolean} [bottom = false] sets the default position to below instead of above
*/
@Component({
  name: 'popover',
})
export default class Popover extends Vue {
  // To specify TypeScript that we got a HTMLElement
  $el!: HTMLElement;

  @Prop({
    type: Boolean,
    required: true,
  })
  visible!: boolean;

  @Prop({
    default: () => Alignment.Center,
    validator: value => Object.values(Alignment).includes(value),
  })
  align!: Alignment;

  @Prop({
    type: Boolean,
    default: () => false,
  })
  bottom!: boolean;

  elementStyle: ElementPosition = {};
  parent: HTMLElement | null = null;
  parents: HTMLElement[] = [];

  get isBottom() {
    return this.bottom;
  }

  hasBeenPositioning = false;

  @Watch('visible', { immediate: true })
  async onVisibleChange(visible: boolean) {
    if (!visible) {
      // `destroyPositioning` must not be called if `setupPositioning` has not been called as well
      if (this.hasBeenPositioning) {
        this.hasBeenPositioning = false;
        this.destroyPositioning();
      }
    } else {
      // We need to wait for the "visible" element to become effectively visible before positioning it.
      await this.$nextTick();
      this.hasBeenPositioning = true;
      this.setupPositioning();
    }
  }

  setupPositioning() {
    // Save original parent before moving into body to use its position
    this.parent = this.$el.parentElement;

    // Move the popover into the body to prevent its hiding by an
    // `overflow: hidden` container
    document.body.appendChild(this.$el);

    // Get all scrollable parents
    const parents = [];
    let { parent } = this;

    while (parent !== document.body && parent !== null) {
      parents.push(parent);
      parent = parent.parentElement;
    }

    this.parents = parents;

    // Attach listeners
    window.addEventListener('click', this.clickListener, { capture: true });
    window.addEventListener('orientationchange', this.orientationchangeListener);
    window.addEventListener('resize', this.resizeListener);
    for (const p of parents) {
      p.addEventListener('scroll', this.scrollListener);
    }
    this.updatePosition();
  }

  /**
   * @description Close the popover when clicking outside
   */
  clickListener(e: Event) {
    if (!this.visible) {
      return;
    }
    const target = e.target as HTMLElement;
    const hasClickOnItSelf = target === this.$el || this.$el.contains(target);

    if (!hasClickOnItSelf) {
      this.$emit('closed');
    }
  }

  // Update position on orientation change
  orientationchangeListener() {
    return this.updatePosition();
  }

  // Update position on resize
  resizeListener() {
    return this.updatePosition();
  }

  // Update position on scroll
  scrollListener() {
    return this.updatePosition();
  }

  // Set the absolute position
  // Checks available space on screen for vertical positioning and alignment
  updatePosition = _.throttle(
    function(this: Popover) {
      if (this.parent === null) {
        return;
      }

      const positionContext = {
        body: document.body.getBoundingClientRect(),
        parent: this.parent.getBoundingClientRect(),
        element: this.$el,
        window,
      };
      // Set alignment
      const elementStyle: ElementPosition = DOMUtil.align(this.align, positionContext);
      elementStyle.top = DOMUtil.computeTop(this.isBottom, positionContext);
      // make sure to use `px` unit explicitly
      this.elementStyle = _.fromPairs(Object.entries(elementStyle).map(([k, v]) => [k, `${v}px`]));
    },
    // 60fps
    16,
  );

  destroyPositioning() {
    // Cleanup listeners
    window.removeEventListener('orientationchange', this.orientationchangeListener);
    window.removeEventListener('resize', this.resizeListener);
    for (const parent of this.parents) {
      parent.removeEventListener('scroll', this.scrollListener);
    }
    // Cleanup DOM
    if (this.$el.parentElement !== null) {
      return this.$el.parentElement.removeChild(this.$el);
    }
  }
}
</script>
<style lang="scss" scoped>
.popover {
  font-family: 'Montserrat', sans-serif;
  position: absolute;
  visibility: visible;
}
</style>
